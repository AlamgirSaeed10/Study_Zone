package com.software.acuity.splick.models.common;

public class UserAuthModel {
    String username;
    String userFullName;
    String userLocation;
    String userBiography;
    String userTags;
    String userPortfolio;
    String userSocialReach;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUserFullName() {
        return userFullName;
    }

    public void setUserFullName(String userFullName) {
        this.userFullName = userFullName;
    }

    public String getUserLocation() {
        return userLocation;
    }

    public void setUserLocation(String userLocation) {
        this.userLocation = userLocation;
    }

    public String getUserBiography() {
        return userBiography;
    }

    public void setUserBiography(String userBiography) {
        this.userBiography = userBiography;
    }

    public String getUserTags() {
        return userTags;
    }

    public void setUserTags(String userTags) {
        this.userTags = userTags;
    }

    public String getUserPortfolio() {
        return userPortfolio;
    }

    public void setUserPortfolio(String userPortfolio) {
        this.userPortfolio = userPortfolio;
    }

    public String getUserSocialReach() {
        return userSocialReach;
    }

    public void setUserSocialReach(String userSocialReach) {
        this.userSocialReach = userSocialReach;
    }
}
