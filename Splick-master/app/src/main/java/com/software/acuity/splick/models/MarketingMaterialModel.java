package com.software.acuity.splick.models;

public class MarketingMaterialModel {

    String id = "";
    String deal_id = "";
    String user_id = "";
    String mat_title = "";
    String mat_name = "";
    String mat_type = "";
    String mat_size = "";
    String mat_details = "";
    String mat_path = "";
    String add_time = "";

    public MarketingMaterialModel() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDeal_id() {
        return deal_id;
    }

    public void setDeal_id(String deal_id) {
        this.deal_id = deal_id;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getMat_title() {
        return mat_title;
    }

    public void setMat_title(String mat_title) {
        this.mat_title = mat_title;
    }

    public String getMat_name() {
        return mat_name;
    }

    public void setMat_name(String mat_name) {
        this.mat_name = mat_name;
    }

    public String getMat_type() {
        return mat_type;
    }

    public void setMat_type(String mat_type) {
        this.mat_type = mat_type;
    }

    public String getMat_size() {
        return mat_size;
    }

    public void setMat_size(String mat_size) {
        this.mat_size = mat_size;
    }

    public String getMat_details() {
        return mat_details;
    }

    public void setMat_details(String mat_details) {
        this.mat_details = mat_details;
    }

    public String getMat_path() {
        return mat_path;
    }

    public void setMat_path(String mat_path) {
        this.mat_path = mat_path;
    }

    public String getAdd_time() {
        return add_time;
    }

    public void setAdd_time(String add_time) {
        this.add_time = add_time;
    }
}
